                        <p>Here is a thoughtful article that is interesting to read</p>
                        <p>US Grand Strategy, R.I.P. in Straits Times 27 Jun.</p>
                        <p>
                            As the author, Adam Garfinkle, writes in this article, the end of Pax Americana is at hand.  The case is well presented.  Of course, there would be some economic fall out for all of us in that event - which he did not cover.  I will do that below.
                        </p>
                        <p>
                            After the Second World War, US foreign policy was generally successful, save for the blunders in Korea and Vietnam.  It brought us to the "End of History", upon the fall of the Berlin Wall in 1990.   The term in quotes is high sounding, and was a bombastic declaration of victory in the capitalist-liberal democratic ideology over all other alternatives of government and economic organisation.   This was the thesis of Francis Fukuyama, a noted thinker of the time.   
                        </p>
                        <p>
                            After the initial euphoria in the decade of the 1990's, America found that it has since lost direction in foreign policy.  There is no longer a "foe" that can focus its intellectual resources and policy energies.   The wonks tried to replace the evil empire with an anti-Muslim "crusade" of sorts, but that is an unequal competition.   Then they moved on to contain China.   But as Garfinkle correctly points out, China can never be the replacement for that challenger which the entire Soviet bloc of nations came to represent during the decades of the Cold War.   This is because economic partnership between the US and China is much higher than it ever was between the Soviets and the Americans.  Now, America is in a geo-political contest with nobody.   It actually needs to rediscover itself.  During this time when America is searching for its soul, it will not have the energy to support far flung overseas adventures, with an open ended checkbook.   That's the gist of Garfinkle's thesis.
                        </p>
                        <p>
                            I agree.   I also agree that Trump, having wreaked havoc with American foreign policy, is not the problem.  The US is discovering how one third of the country, the Trump base, which can variously be described as the religious right, the conservatives or whatever, is no different from the isolationists that dominated American foreign policy between the First and Second World Wars. This one-third can push the whole nation in a way that the other two-thirds may not want to go.   Post-Trump, the Americans will have to decide on a new direction.
                        </p>
                        <p>
                            But the damage wrought by four years of neglect is not insignificant.  The lack of a comparable challenger during the three decades after the Berlin Wall has led to expensive and inexcusable forays into Iraq and Afghanistan, both minuscule threats to the American empire.   But unlike the Israelis who have made it an art to keep their wars short, with containable costs, the US has wasted trillions in their misadventures and is now unable to revert to its pre 1990 greatness even if it wants to.  Ten years in Iraq and seventeen years in Afghanistan have passed and in the latter, it is still counting, the longest war ever in American history.
                        </p>
                        <p>
                            So in this discussion, the first thing we need to recognise about the America that is trying to get out of its global obligations is that it can no longer afford it - it has squandered a humongous amount of money with nothing but a huge budget deficit to show for it.
                        </p>
                        <p>
                            Trump is one result of that disconnect between thirty years of not having a worthy enemy like the Soviet bloc to fight and what many Americans like to think should be the right of global leadership upon defeating communism.  They had not experienced this emptiness, essentially, unlike after WW2, when they were able to enjoy a generation of unprecedented prosperity, helping to rebuild war-torn Germany and Japan.  But as soon as the evil empire was defeated in the 1990s, there was a conspicuous absence of a peace dividend.   All they got this last time around was a Japan who resurrected to become "Number One" for a time, and then when that was overcome, a bigger economic competitor emerged in the form of a China that was no longer communist.  The fruits of victory in the Cold War seemed to be very elusive.   There was only the "war on terror" from a handful of dispossessed Muslims, a disjointed fight in which nuclear weapons and the strongest military in the world cannot be brought to bear.   There is a huge frustration that vast economic wealth is being squandered with no clear victories in a war with no front lines.  Desert Storm in 1991 was the last hurrah.   That is the starting point of the American economic decline - failed military ventures.  
                        </p>
                        <p>
                            Then there is the exuberance of consumerism that is the American way of life.   They spend too much.  How did it become like that?   Nobody seems to have savings; they spend every penny they earn immediately.  This has created huge debt levels economy wide.   The trade deficits are also very large as a result of American consumerism. 
                        </p>
                        <p>
                            We add to this whole background the tremendous debt burdens created by successive financial disasters - all created, executed and imploded in America.   First there was the LTCM disaster in 1998 (this was a hedge fund blow-up for those who are not in finance) which threatened the entire bond market and was the first step in the US Federal Reserve assuming the moral obligation to unmake financial disasters. The concept of "too big to fail" emerged, which is strangely a socialist idea, rather than a capitalist one.   Just about a decade later, the mortgage crisis of 2008 hit,  which was linked to the LTCM fallout due to the abundant liquidity generated by Greenspan (Fed Chairman at that time) policies.  Now not just monetary policy, but fiscal policy in the US became tied to Wall Street.  Trillions of dollars were thrown at that problem. It did save America but when we look deeper, the America that was admired in the post WW2 years - a paragon of economic management - is gone.   Loose monetary policy (which acquired the tag "quantitative easing") became the order of the day.
                        </p>
                        <p>
                            Just as the country seems to settle down to a new meaning of the "end of (financial) history", Covid- 19 consumed America in 2020, left unmanaged by an Administration under Trump that was put there by voters tired of the financial obligations of wars, economic competition they cannot win, and global foreign policy leadership they lost control of and desperately wanting to become isolationist again.  Trump is not of the temperament to solve problems like Covid19.   And we see the Fed double down on rescuing the economy hit badly by the pandemic.  At this time, Pax Americana looks like it is in shambles.
                        </p>
                        <p>
                            And if there is something in the stock market that you need to put your money into, Warren Buffett's advice on buying ETFs on the S&P (on dips whenever this happens) is as good as any.  
                        </p>
